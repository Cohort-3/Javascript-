// Airthematic Operators

let a = 10;
let b = 20;

console.log(a+b);   // Output: 30
console.log(a-b);   // Output: -10
console.log(a*b);   // Output: 200
console.log(a/b);   // Output: 0.5
console.log(a%b);   // Output: 10
console.log(a**b);  // Output: 100000000000000000000


// Assignment Operators

let score = 50;
score += 10;
console.log(score);  // Output: 60

score -= 10;    
console.log(score);  // Output: 40

let bonus = 20;
bonus *= 2;
console.log(bonus);  // Output: 40  

let c = 10;
c /= 2;
console.log(c);  // Output: 5   



// Comparison Operators

let age = 20;
console.log(age == 20);  // Output: true
console.log(age != 20);  // Output: false
console.log(age > 20);  // Output: false
console.log(age < 20);  // Output: false
console.log(age >= 20);  // Output: true
console.log(age <= 20);  // Output: true


// Logical Operators

let isLoggedIn = true;
let isAdmin = false;

console.log(isLoggedIn && isAdmin);  // Output: false
console.log(isLoggedIn || isAdmin);  // Output: true
console.log(!isLoggedIn);  // Output: false